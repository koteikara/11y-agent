#!/usr/bin/env node
"use strict";

const fs = require("fs");
const path = require("path");
const {
  STRUCTURAL_KEYS,
  autoFixHtml,
  collectMetrics,
  compareHtml,
  visibleText,
} = require("../lib/sagaAutoFix");

const rootDir = path.resolve(__dirname, "..");

function parseArgs(argv) {
  const args = {
    dataset: path.join(rootDir, "agents-cli", "datasets", "saga-a11y-eval.jsonl"),
    outputDir: path.join(rootDir, "agents-cli", "results"),
    writeHtml: false,
    json: false,
  };

  for (let index = 2; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--dataset") {
      args.dataset = path.resolve(argv[++index]);
    } else if (arg === "--output-dir") {
      args.outputDir = path.resolve(argv[++index]);
    } else if (arg === "--write-html") {
      args.writeHtml = true;
    } else if (arg === "--json") {
      args.json = true;
    } else if (arg === "--help" || arg === "-h") {
      args.help = true;
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }
  return args;
}

function usage() {
  return [
    "Usage: node tools/run-agents-cli-local-eval.js [options]",
    "",
    "Options:",
    "  --dataset <file>     JSONL dataset generated by build-agents-cli-dataset.js",
    "  --output-dir <dir>   Output directory for traces and grade results",
    "  --write-html         Write current HTML outputs by case",
    "  --json               Print JSON summary",
  ].join("\n");
}

function readJsonl(filePath) {
  return fs
    .readFileSync(filePath, "utf8")
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter(Boolean)
    .map((line) => JSON.parse(line));
}

function round(value) {
  return Math.round(Number(value || 0) * 10000) / 10000;
}

function gradeCase(testCase) {
  const oldHtml = testCase.input?.old_html || "";
  const goldHtml = testCase.expected?.gold_html || "";
  const currentHtml = autoFixHtml(oldHtml);
  const comparison = compareHtml(currentHtml, goldHtml, oldHtml);
  const structuralMatches = STRUCTURAL_KEYS.filter((key) => comparison.metrics[key].status === "matches_gold").length;
  const structuralRatio = structuralMatches / STRUCTURAL_KEYS.length;
  const score = round((comparison.currentSimilarity * 0.6 + structuralRatio * 0.4) * 100);
  const failures = STRUCTURAL_KEYS.filter((key) => comparison.metrics[key].status !== "matches_gold").map((key) => ({
    metric: key,
    status: comparison.metrics[key].status,
    baseline: comparison.metrics[key].baseline,
    current: comparison.metrics[key].current,
    gold: comparison.metrics[key].gold,
  }));

  return {
    id: testCase.id,
    score,
    output_html: currentHtml,
    comparison: {
      baseline_similarity: round(comparison.baselineSimilarity),
      current_similarity: round(comparison.currentSimilarity),
      similarity_delta: round(comparison.similarityDelta),
      structural_matches: structuralMatches,
      structural_total: STRUCTURAL_KEYS.length,
      improved_metrics: comparison.improved,
      regressed_metrics: comparison.regressed,
      differs_from_gold: comparison.differsFromGold,
    },
    metrics: {
      old: collectMetrics(oldHtml),
      current: collectMetrics(currentHtml),
      gold: collectMetrics(goldHtml),
      details: comparison.metrics,
    },
    failures,
    text_lengths: {
      old: visibleText(oldHtml).length,
      current: visibleText(currentHtml).length,
      gold: visibleText(goldHtml).length,
    },
  };
}

function summarize(results, args) {
  const totals = results.reduce(
    (acc, result) => {
      acc.score += result.score;
      acc.currentSimilarity += result.comparison.current_similarity;
      acc.baselineSimilarity += result.comparison.baseline_similarity;
      acc.similarityDelta += result.comparison.similarity_delta;
      acc.improvedMetrics += result.comparison.improved_metrics;
      acc.regressedMetrics += result.comparison.regressed_metrics;
      acc.differsFromGold += result.comparison.differs_from_gold;
      return acc;
    },
    {
      cases: results.length,
      score: 0,
      currentSimilarity: 0,
      baselineSimilarity: 0,
      similarityDelta: 0,
      improvedMetrics: 0,
      regressedMetrics: 0,
      differsFromGold: 0,
    }
  );

  const metricSummary = Object.fromEntries(
    STRUCTURAL_KEYS.map((key) => [
      key,
      {
        matches_gold: 0,
        improved: 0,
        regressed: 0,
        unchanged_or_differs: 0,
      },
    ])
  );

  for (const result of results) {
    for (const key of STRUCTURAL_KEYS) {
      const status = result.metrics.details[key].status;
      if (status === "matches_gold") metricSummary[key].matches_gold += 1;
      else if (status === "improved") metricSummary[key].improved += 1;
      else if (status === "regressed") metricSummary[key].regressed += 1;
      else metricSummary[key].unchanged_or_differs += 1;
    }
  }

  return {
    run_id: `goal2_agents_cli_local_${new Date().toISOString().replace(/[:.]/g, "-")}`,
    dataset: args.dataset,
    output_dir: args.outputDir,
    totals: {
      cases: totals.cases,
      average_score: round(totals.score / Math.max(1, totals.cases)),
      average_baseline_similarity: round(totals.baselineSimilarity / Math.max(1, totals.cases)),
      average_current_similarity: round(totals.currentSimilarity / Math.max(1, totals.cases)),
      average_similarity_delta: round(totals.similarityDelta / Math.max(1, totals.cases)),
      improved_metrics: totals.improvedMetrics,
      regressed_metrics: totals.regressedMetrics,
      differs_from_gold: totals.differsFromGold,
    },
    metric_summary: metricSummary,
    worst_cases: [...results]
      .sort((a, b) => b.comparison.differs_from_gold - a.comparison.differs_from_gold || a.score - b.score)
      .slice(0, 10)
      .map((result) => ({
        id: result.id,
        score: result.score,
        current_similarity: result.comparison.current_similarity,
        differs_from_gold: result.comparison.differs_from_gold,
        regressed_metrics: result.comparison.regressed_metrics,
        failures: result.failures.slice(0, 6),
      })),
  };
}

function writeOutputs(results, summary, args) {
  fs.mkdirSync(args.outputDir, { recursive: true });
  const tracePath = path.join(args.outputDir, "saga-a11y-local-traces.jsonl");
  const gradePath = path.join(args.outputDir, "saga-a11y-local-grade.json");
  const reportPath = path.join(args.outputDir, "saga-a11y-local-report.md");

  fs.writeFileSync(tracePath, `${results.map((result) => JSON.stringify(result)).join("\n")}\n`, "utf8");
  fs.writeFileSync(gradePath, JSON.stringify({ summary, results }, null, 2), "utf8");
  fs.writeFileSync(reportPath, renderMarkdown(summary), "utf8");

  if (args.writeHtml) {
    const htmlDir = path.join(args.outputDir, "html");
    fs.mkdirSync(htmlDir, { recursive: true });
    for (const result of results) {
      const fileName = `${result.id.split("/").pop()}`;
      fs.writeFileSync(path.join(htmlDir, fileName), result.output_html, "utf8");
    }
  }

  return { tracePath, gradePath, reportPath };
}

function renderMarkdown(summary) {
  const lines = [
    "# agents-cli Local Evaluation Report",
    "",
    `- Dataset: \`${summary.dataset}\``,
    `- Cases: ${summary.totals.cases}`,
    `- Average score: ${summary.totals.average_score}`,
    `- Text similarity: ${summary.totals.average_baseline_similarity} -> ${summary.totals.average_current_similarity} (${formatDelta(summary.totals.average_similarity_delta)})`,
    `- Improved metrics: ${summary.totals.improved_metrics}`,
    `- Regressed metrics: ${summary.totals.regressed_metrics}`,
    `- Structural differs from gold: ${summary.totals.differs_from_gold}`,
    "",
    "## Metric Summary",
    "",
    "| Metric | Matches gold | Improved | Regressed | Other differs |",
    "|---|---:|---:|---:|---:|",
  ];

  for (const key of STRUCTURAL_KEYS) {
    const item = summary.metric_summary[key];
    lines.push(`| ${key} | ${item.matches_gold} | ${item.improved} | ${item.regressed} | ${item.unchanged_or_differs} |`);
  }

  lines.push("", "## Worst Cases", "");
  for (const item of summary.worst_cases) {
    lines.push(`- ${item.id}: score=${item.score}, similarity=${item.current_similarity}, differs=${item.differs_from_gold}, regressed=${item.regressed_metrics}`);
  }

  return `${lines.join("\n")}\n`;
}

function formatDelta(value) {
  return value >= 0 ? `+${round(value)}` : String(round(value));
}

function printText(summary, outputPaths) {
  console.log(`Cases: ${summary.totals.cases}`);
  console.log(`Average score: ${summary.totals.average_score}`);
  console.log(
    `Similarity: ${summary.totals.average_baseline_similarity} -> ${summary.totals.average_current_similarity} (${formatDelta(summary.totals.average_similarity_delta)})`
  );
  console.log(`Trace: ${outputPaths.tracePath}`);
  console.log(`Grade: ${outputPaths.gradePath}`);
  console.log(`Report: ${outputPaths.reportPath}`);
}

function main() {
  const args = parseArgs(process.argv);
  if (args.help) {
    console.log(usage());
    return;
  }
  const cases = readJsonl(args.dataset);
  const results = cases.map(gradeCase);
  const summary = summarize(results, args);
  const outputPaths = writeOutputs(results, summary, args);
  if (args.json) {
    console.log(JSON.stringify({ summary, outputPaths }, null, 2));
  } else {
    printText(summary, outputPaths);
  }
}

if (require.main === module) {
  try {
    main();
  } catch (error) {
    console.error(error.stack || error.message);
    process.exitCode = 1;
  }
}

module.exports = {
  gradeCase,
  summarize,
};
